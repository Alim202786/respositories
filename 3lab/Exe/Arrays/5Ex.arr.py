#Удаление элементов массива
#Вы можете использовать pop() метод для удаления элемента из массива
cars = ["Ford", "Volvo", "BMW"]
cars.pop(1)                          #Удалить второй элемент массива cars
print(cars)


#Вы также можете использовать этот remove() метод для удаления элемента из массива
cars = ["Ford", "Volvo", "BMW"]
cars.remove("BMW")                #Удалить элемент со значением «BMW»
print(cars)