#Число с плавающей точкой (Float)
x = 1.10
y = 1.0
z = -35.59

print(type(x))
print(type(y))
print(type(z))


#Число с плавающей точкой также может быть научным числом с буквой «e», указывающей степень числа 10.
x = 35e3
y = 12E4
z = -87.7e100

print(type(x))
print(type(y))
print(type(z))
