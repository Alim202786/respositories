"""
String Concatenation
"""
#Для объединения или конкатенации двух строк можно использовать оператор +
#1
a = "Hello"
b = "World"
c = a + b     #Объединить переменную a с переменной b в переменную c
print(c)


#2
a = "Hello"
b = "World"
c = a + " " + b       #Чтобы добавить пробел между ними, добавьте " "
print(c)
