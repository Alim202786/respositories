"""
Split String
"""
#Метод split()возвращает список, в котором текст между указанным разделителем становится элементами списка
 
a = "Hello, World!"             
print(a.split(","))           #Метод split()разбивает строку на подстроки, если находит экземпляры разделителя
#returns ['Hello', ' World!']